<h3 id="登录与账号问题" tabindex="-1"><a class="header-anchor" href="#登录与账号问题"><span>登录与账号问题</span></a></h3>
<p><strong>Q: 点击登录无反应怎么办？</strong></p>
<p>A:</p>
<ul>
<li>尝试切换登录线路（通过登录页面的下拉菜单）。</li>
<li>更换网络环境（例如，从 Wi-Fi 切换到移动数据，反之亦然）。</li>
<li>在手机设置中搜索并执行“还原所有设置”（注意：这仅还原设置，不删除数据）。</li>
<li>耐心等待，可能是服务器暂时出现问题或受到攻击。</li>
</ul>
<p><strong>Q: 登录提示“风控”怎么办？</strong></p>
<p>A: 请根据软件内的提示联系客服 QQ 处理。</p>
<p><strong>Q: 登录提示“请在原设备登录”怎么办？</strong></p>
<p>A: 这通常发生在清除数据、售后降级或更换设备后。</p>
<ul>
<li>您需要先在原设备或通过解绑网页（点击登录页面的“解绑”按钮跳转）进行解绑。</li>
<li>每张卡密只有 3 次解绑机会，请谨慎使用并保管好卡密。若次数用尽，需要重新购买。</li>
</ul>
<p><strong>Q: 登录提示“服务器维护”怎么办？</strong></p>
<p>A: 这是正常情况，表示服务器正在维护中，请耐心等待维护完成。</p>
<p><strong>Q: 为什么会被风控？</strong></p>
<p>A: 通常是因为系统检测到您设备上开启了 VPN、游戏加速器、Clash 等代理工具。请关闭这些工具后再尝试登录。如有疑问，请联系软件提示的客服 QQ。</p>
<h3 id="使用与功能问题" tabindex="-1"><a class="header-anchor" href="#使用与功能问题"><span>使用与功能问题</span></a></h3>
<p><strong>Q: 我是 root 用户，已经有其他去温控模块了，还需要使用 vivo 温控引擎吗？</strong></p>
<p>A: 本软件为 root 用户提供了专门的支持和优化：</p>
<ul>
<li>点击软件主界面的 🌡️ 图标即可触发 root 去温控。</li>
<li>本软件的去温控方案经过长期测试，旨在解决其他模块可能导致的卡顿掉帧问题，在解除温控的同时保证系统流畅。</li>
<li>Root 用户可以使用“游戏模式”并获取专属的“伪装温度”功能（伪装温度功能本身不推荐普通用户使用）。</li>
<li><strong>注意：</strong> 通过本软件进行的去温控操作（包括 Root 方式），在手机重启后会失效，需要重新进入软件操作。</li>
</ul>
<p><strong>Q: 如何选择合适的“开始解除-XXX”温控方案？</strong></p>
<p>A: 请参考软件内的建议：</p>
<ul>
<li>iQOO 数字系列 (OS 3) 建议使用“推荐方案”。</li>
<li>天玑机型、老设备 (8G1 以下) 建议使用“备用方案”。</li>
<li>OriginOS Ocean 可使用“Ocean 方案”。</li>
<li>OS 4 或其他方案效果不好时，请尝试“OS4(兼容)”方案（通常首次使用所有系统都执行此方案，执行后需重启）。</li>
<li><strong>注意：</strong> 只会有一个方案生效（最后点击的那个），游戏前执行去温控，每次进游戏都需要执行一遍。</li>
</ul>
<p><strong>Q: 如何使用“解除锁帧-XXX”功能？</strong></p>
<p>A: 当游戏中出现锁帧时，通过小窗打开本软件，点击“解除锁帧”并选择您期望的游戏帧率。也可以在感觉快要锁帧时提前操作以尝试延缓。</p>
<ul>
<li><strong>注意：</strong> 系统配置可能会覆盖此设置导致再次锁帧。此功能并非保证有效，且对于因温度过高导致的严重锁帧（锁死），必须配合散热器（如手机背夹）使用。</li>
</ul>
<p><strong>Q: 如何使用“游戏模式”？</strong></p>
<p>A: 需要在进入游戏后，通过游戏小窗启动本软件来执行“游戏模式”。其具体效果与您在软件右上角齿轮设置中的配置（如 m+、触控优化、伪装温度、游戏魔盒开关）相关。强烈推荐使用此模式以增强性能和优化温控效果。</p>
<ul>
<li><strong>注意：</strong> 执行游戏模式后，请尽量避免切后台、使用小窗等行为，否则可能导致模式失效，需要重新进入游戏并执行。不玩游戏时建议关闭以省电。</li>
</ul>
<p><strong>Q: 温度计 🌡️ 图标有什么用？</strong></p>
<p>A: 此功能用于尝试优化去温控效果，必须配合主界面的“开始解除-XXX”去温控功能一起使用。</p>
<ul>
<li>OriginOS 4.0 及以上系统通常无需手动点击。</li>
<li>Root 用户可以手动点击开启（点击一次即可，重启后无需再次点击）。</li>
<li><strong>注意：</strong> 请勿连续点击，可能导致应用卡死。</li>
</ul>
<p><strong>Q: 左上角的太阳 ☀️ 图标有什么用？</strong></p>
<p>A: 用于解决游戏因高温触发的自动降低亮度问题。当游戏中发现亮度降低时，通过小窗打开本软件并点击此图标即可。</p>
<ul>
<li><strong>注意：</strong> 只能解决自动降亮度，不能恢复到手动最高亮度或激发亮度。在较低版本 OriginOS 上配合冻结 i 管家及插件效果可能更佳。</li>
</ul>
<p><strong>Q: “平台优化”（联发科/骁龙图标）功能建议使用吗？</strong></p>
<p>A: 这是实验性功能，不保证稳定性。如果“去温控”和“游戏模式”已满足您的需求，则不建议使用。新平台（如 D9400, 8Elite）不需要使用。此功能与“满血调度+”互斥，切换或停用可能需要卸载“智慧引擎”更新、重装本应用并重启手机。</p>
<p><strong>Q: “超感触控”有什么用？</strong></p>
<p>A: 为硬件支持但系统阉割或游戏不支持的场景强制开启超感触控。此功能依赖游戏魔盒。</p>
<p><strong>Q: “触控参数优化”有什么用？</strong></p>
<p>A: 点击后可优化全局触摸响应，提升跟手度和顺滑感。</p>
