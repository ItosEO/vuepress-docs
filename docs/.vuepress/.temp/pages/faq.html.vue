<p><strong>Q: 系统优化 Pro 如何使用？</strong></p>
<p>A:</p>
<ol>
<li>确保 Shizuku 已激活并授权给本软件。</li>
<li>（首次使用或之前用过免费版）建议先在软件内点击“一键还原”，然后重启手机。</li>
<li>重启后，再次确保 Shizuku 激活，登录软件，点击“系统优化 Pro”按钮即可。此优化适用于日用和游戏。</li>
<li>户外场景可直接执行优化以替换省电模式，无需重启或还原。</li>
</ol>
<p><strong>Q: 系统优化 Pro 需要一直挂在后台吗？</strong></p>
<p>A: 不需要。优化执行完毕后即可关闭软件后台，优化效果不会丢失。</p>
<p><strong>Q: 重启手机后优化效果还在吗？</strong></p>
<p>A: 不在。重启手机后优化会失效，需要重新激活 Shizuku 并进入软件点击优化按钮。</p>
<p><strong>Q: Root 用户能使用系统优化 Pro 吗？</strong></p>
<p>A: 可以。Root 用户可以正常使用系统优化 Pro。该版本旨在解决流畅度、触控、续航、卡顿等问题，提升 OriginOS 使用体验。同样，重启后优化会失效，需要重新执行。</p>
<p><strong>Q: “备份还原”和“一键还原”有什么区别？</strong></p>
<p>A: “备份还原”更安全稳定，用于恢复到之前的备份状态或默认状态。“一键还原”是通过删除的方式恢复默认设置。推荐使用“备份还原”来恢复默认。操作后需要重启手机。</p>
